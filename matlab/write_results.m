function write_results(fileID,all_annos,annotated_car_id,target,id_to_tire)
   fieldname = sprintf("obj_%s",int2str(annotated_car_id));
   origin = all_annos.(fieldname).tires_3D.(id_to_tire(all_annos.(fieldname).car_origin));
   %Take care of case no wheelbase
   if ~isfield(all_annos.(fieldname),'wheelbase')
       all_annos.(fieldname).wheelbase = -1;
   end

   to_write = [target annotated_car_id ...
       all_annos.(fieldname).num_sym_pairs ...
       all_annos.(fieldname).bbox_2D_height ...
       all_annos.(fieldname).reproj_error ...
       all_annos.(fieldname).gt_heading_angle,...
       all_annos.(fieldname).pred_heading_angle,...
       all_annos.(fieldname).angle_difference,...
       all_annos.(fieldname).dist_base_gt_bbox ...
       all_annos.(fieldname).dist_base_pred_bbox,...
       all_annos.(fieldname).dist_base_bbox_diff,...
       all_annos.(fieldname).dist_nearest_corner_gt_bbox,...
       all_annos.(fieldname).dist_nearest_corner_pred_bbox,...
       all_annos.(fieldname).dist_nearest_corner_diff,...
       all_annos.(fieldname).iou,...
       all_annos.(fieldname).iou_bev,...
       all_annos.(fieldname).mounting_height,...
       all_annos.(fieldname).ds,...
       all_annos.(fieldname).time_diff,...
       all_annos.(fieldname).pred_length_complete_box,...
       all_annos.(fieldname).pred_width_complete_box,...
       all_annos.(fieldname).pred_height_complete_box,...
       all_annos.(fieldname).wheelbase,...
       all_annos.(fieldname).gt_length,...
       all_annos.(fieldname).gt_width,...
       all_annos.(fieldname).gt_height,...
       all_annos.(fieldname).pred_width_wo_mirrors,...
       all_annos.(fieldname).tire_both_sides,...
       all_annos.(fieldname).has_mirror,...
       all_annos.(fieldname).dist_to_move,...
       all_annos.(fieldname).gt_width_nonpropagate,...
       all_annos.(fieldname).gt_height_nonpropagate,...
       all_annos.(fieldname).gt_length_nonpropagate,...
       all_annos.(fieldname).get_length_gaussian,...
       size(all_annos.(fieldname).tire_ids,2)
       ];
   fprintf(fileID,['%s %s %.0f %.0f %.2f %.2f %.2f %.2f %.2f %.2f %.2f %.2f' ...
       ' %.2f %.2f %.2f %.2f %.2f %.2f %.0f %.2f %.2f %.2f %.2f %.2f %.2f %.2f %.2f %.0f %.0f %.2f %.2f %.2f %.2f %.0f %.0f\n'],to_write);
end